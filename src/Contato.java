
public class Contato {
	
	//Atributo
	private int tipo;
	private int valor;
	
	//Metodo construtor
	public Contato(int tipo, int valor) {
		super();
		this.tipo = tipo;
		this.valor = valor;
	}
	
	//Get e Set
	public int getTipo() {
		return tipo;
	}

	public void setTipo(int tipo) {
		this.tipo = tipo;
	}

	public int getValor() {
		return valor;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}
	
	
	
	
	
	
	
	
	

}
